import Foundation

/// One language's labeled examples for training the intent head: lines that read
/// as a question/task for the AI (`ask`) vs. lines that read as a personal jot to
/// file away (`note`).
struct IntentExampleSet {
    let ask: [String]
    let note: [String]
}

/// The training corpus for `IntentEngine` — curated, hand-labeled examples of what
/// people actually type into a launcher-style input, in the two languages the app
/// primarily serves. These are NOT matched against the user's text (this is not a
/// lexicon); they are embedded once with `NLContextualEmbedding` and used to fit a
/// tiny logistic head over the embedding space. Quality and *diversity* of these
/// lines is what the classifier's judgment is made of, so the sets deliberately
/// include the hard boundary pairs a rules engine can't tell apart:
///
///   · "帮我写邮件给老板" (a task for the AI → ask)  vs "发邮件给老板" (a todo → note)
///   · "买什么牛奶好" (ask)                          vs "买牛奶" (note)
///   · "明天会下雨吗" (ask)                          vs "明天交房租" (note)
///   · questions with no question mark, jots that contain "recommend"/"提醒", etc.
///
/// Mixed-language lines ("review 一下我的简历") live in the Chinese set because any
/// Han character routes the text to the CJK embedding model (see `IntentEngine`).
enum IntentExamples {
    static let chinese = IntentExampleSet(ask: chineseAsk, note: chineseNote)
    static let english = IntentExampleSet(ask: englishAsk, note: englishNote)

